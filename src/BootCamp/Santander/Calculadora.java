package BootCamp.Santander;

public class Calculadora {

    public static void soma(double numero1, double numero2){

        double resultado = numero1 + numero2;
        System.out.println("A soma de " + numero1 + " mais " + numero2 + " é " + resultado);

    }

    public static void subtracao(double numero1, double numero2){

        double resultado = numero1 - numero2;

        System.out.println("A subtracao de " + numero1 + " menos " + numero2 + " é " + resultado);
    }


    public static void multiplicacao(double num1, double num2){

        double resultado = num1 * num2;
        System.out.println("A multiplicacao de " + num1 + " vezes " + num2 + " é " + resultado);

    }

    public static void divisao(double numero1, double numero2){

        double resultado = numero1 * numero2;
        System.out.println("A multiplicacao de " + numero1 + " por " + numero2 + " é " + resultado);

    }




}
